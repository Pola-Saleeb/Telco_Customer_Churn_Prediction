import streamlit as st
import pandas as pd


def report_page(df, model, features):

    st.title("📄 Manager Report")

    st.write(
        "Generate an executive summary of the current customer churn situation."
    )

    # -----------------------------
    # Prepare Data
    # -----------------------------

    data = df.copy()

    data["TotalCharges"] = pd.to_numeric(
        data["TotalCharges"],
        errors="coerce"
    )

    data["TotalCharges"] = data["TotalCharges"].fillna(
        data["TotalCharges"].median()
    )

    encoded = pd.get_dummies(
        data.drop(
            ["customerID", "Churn"],
            axis=1
        ),
        drop_first=True
    )

    encoded = encoded.reindex(
        columns=features,
        fill_value=0
    )

    # -----------------------------
    # Predict Churn Probability
    # -----------------------------

    probabilities = model.predict_proba(encoded)[:, 1]

    data["Probability"] = probabilities

    # -----------------------------
    # Statistics
    # -----------------------------

    total_customers = len(data)

    churn_customers = len(
        data[data["Churn"] == "Yes"]
    )

    churn_rate = (
        churn_customers / total_customers
    ) * 100

    high_risk = data[
        data["Probability"] >= 0.75
    ]

    revenue_at_risk = high_risk[
        "MonthlyCharges"
    ].sum()

    # -----------------------------
    # Top Reasons
    # -----------------------------

    top_contract = (
        data["Contract"]
        .value_counts()
        .idxmax()
    )

    top_payment = (
        data["PaymentMethod"]
        .value_counts()
        .idxmax()
    )

    top_internet = (
        data["InternetService"]
        .value_counts()
        .idxmax()
    )

    # -----------------------------
    # Report
    # -----------------------------

    report = f"""
CUSTOMER CHURN REPORT

=====================================

Total Customers:
{total_customers}

Current Churn Rate:
{churn_rate:.2f}%

High Risk Customers:
{len(high_risk)}

Estimated Revenue At Risk:
${revenue_at_risk:,.2f} per month

-------------------------------------

Most Common Contract:
{top_contract}

Most Common Payment Method:
{top_payment}

Most Common Internet Service:
{top_internet}

-------------------------------------

MANAGEMENT RECOMMENDATIONS

1. Contact high-risk customers immediately.

2. Promote One-Year and Two-Year contracts.

3. Offer discounts to customers with high monthly charges.

4. Improve customer support quality.

5. Encourage automatic payment methods.

=====================================

Generated by Customer Churn Intelligence Platform
"""

    # -----------------------------
    # Show Report
    # -----------------------------

    st.text_area(
        "Manager Report",
        report,
        height=500
    )

    # -----------------------------
    # Download Button
    # -----------------------------

    st.download_button(
        label="📥 Download Report",
        data=report,
        file_name="Customer_Churn_Report.txt",
        mime="text/plain"
    )

    # -----------------------------
    # High Risk Customers
    # -----------------------------

    st.subheader("🚨 Top 10 High-Risk Customers")

    top10 = high_risk.sort_values(
        "Probability",
        ascending=False
    )[[
        "customerID",
        "MonthlyCharges",
        "Probability"
    ]].head(10)

    top10["Probability"] = (
        top10["Probability"] * 100
    ).round(2)

    st.dataframe(
        top10,
        use_container_width=True
    )